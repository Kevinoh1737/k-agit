function getUILang() {
    const saved = localStorage.getItem('kvoice_ui_lang');
    if (saved === 'ko') {
        localStorage.setItem('kvoice_ui_lang', 'en');
        return 'en';
    }
    if (saved && I18N[saved]) return saved;

    const nav = navigator.language || navigator.userLanguage || 'en';
    const short = nav.split('-')[0].toLowerCase();
    if (short === 'ko') return 'en';
    if (I18N[short]) return short;

    return 'en';
}

function setUILang(lang) {
    if (!I18N[lang]) lang = 'en';
    localStorage.setItem('kvoice_ui_lang', lang);
}

function t(key) {
    const lang = getUILang();
    return (I18N[lang] && I18N[lang][key]) || (I18N['en'] && I18N['en'][key]) || key;
}

function tCategory(koreanCat) {
    const key = CATEGORY_MAP[koreanCat];
    if (key) return t(key);
    return koreanCat;
}
