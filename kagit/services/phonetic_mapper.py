PHONEME_TOKENS = [
    'kk', 'tt', 'pp', 'ss', 'jj', 'ch', 'ng',
    'ae', 'ya', 'yae', 'eo', 'yeo', 'ye', 'wa', 'wae', 'oe',
    'yo', 'wo', 'we', 'wi', 'yu', 'eu', 'ui',
    'g', 'n', 'd', 'r', 'l', 'm', 'b', 's', 'j', 'k', 't', 'p', 'h',
    'a', 'e', 'o', 'u', 'i'
]

PHONEME_TOKENS.sort(key=lambda x: -len(x))

CONSONANTS = {'g', 'kk', 'n', 'd', 'tt', 'r', 'l', 'm', 'b', 'pp', 's', 'ss', 'j', 'jj', 'ch', 'k', 't', 'p', 'h'}
VOWEL_TOKENS = {'a', 'ae', 'ya', 'yae', 'eo', 'e', 'yeo', 'ye', 'o', 'wa', 'wae', 'oe', 'yo', 'u', 'wo', 'we', 'wi', 'yu', 'eu', 'ui', 'i'}
CODA_TOKENS = {'k', 'n', 't', 'l', 'm', 'p', 'ng'}


def tokenize_phonetic(text):
    tokens = []
    i = 0
    while i < len(text):
        if text[i] == ' ':
            tokens.append(' ')
            i += 1
            continue

        if text[i] == '-':
            tokens.append('-')
            i += 1
            continue

        matched = False
        for token in PHONEME_TOKENS:
            if text[i:i+len(token)] == token:
                tokens.append(token)
                i += len(token)
                matched = True
                break

        if not matched:
            tokens.append(text[i])
            i += 1

    return tokens


def parse_syllables_from_tokens(tokens):
    syllables = []
    i = 0
    while i < len(tokens):
        tok = tokens[i]
        if tok == ' ':
            syllables.append(' ')
            i += 1
            continue
        if tok == '-':
            i += 1
            continue

        onset = ''
        vowel = ''
        coda = ''

        if tok in CONSONANTS:
            onset = tok
            i += 1
            if i < len(tokens) and tokens[i] in VOWEL_TOKENS:
                vowel = tokens[i]
                i += 1
            else:
                syllables.append({'onset': onset, 'vowel': '', 'coda': ''})
                continue
        elif tok in VOWEL_TOKENS:
            vowel = tok
            i += 1
        else:
            syllables.append(tok)
            i += 1
            continue

        if i < len(tokens) and tokens[i] in CODA_TOKENS:
            is_coda = True
            peek = i + 1
            while peek < len(tokens) and tokens[peek] == '-':
                peek += 1
            if peek < len(tokens):
                nxt = tokens[peek]
                if nxt in CONSONANTS:
                    peek2 = peek + 1
                    while peek2 < len(tokens) and tokens[peek2] == '-':
                        peek2 += 1
                    if peek2 < len(tokens) and tokens[peek2] in VOWEL_TOKENS:
                        is_coda = True
                    else:
                        is_coda = True
                elif nxt in VOWEL_TOKENS:
                    is_coda = False
                elif nxt == ' ':
                    is_coda = True
            if is_coda:
                coda = tokens[i]
                i += 1

        syllables.append({'onset': onset, 'vowel': vowel, 'coda': coda})

    return syllables


def convert_syllable_script(syl, lang_data):
    if isinstance(syl, str):
        return syl

    syllable_map = lang_data.get('syllable_map', {})
    final_map = lang_data.get('final_map', {})

    onset = syl['onset']
    vowel = syl['vowel']
    coda = syl['coda']

    body_key = onset + vowel
    body_result = syllable_map.get(body_key, '')

    if not body_result and vowel:
        body_result = syllable_map.get(vowel, body_key)

    coda_result = final_map.get(coda, coda) if coda else ''

    return body_result + coda_result


def convert_to_language(phonetic_text, language):
    if language == 'english':
        lang_data = LANGUAGE_MAPS.get(language, {})
        lang_map = lang_data.get('map', {})
        if not lang_map:
            return phonetic_text
        tokens = tokenize_phonetic(phonetic_text)
        result = []
        for token in tokens:
            if token == '-':
                result.append('-')
            elif token in lang_map:
                result.append(lang_map[token])
            else:
                result.append(token)
        return ''.join(result)

    lang_data = LANGUAGE_MAPS.get(language)
    if not lang_data:
        return phonetic_text

    script = lang_data.get('script', 'latin')

    if script == 'latin':
        lang_map = lang_data.get('map', {})
        tokens = tokenize_phonetic(phonetic_text)
        result = []
        for token in tokens:
            if token == '-':
                result.append('-')
            elif token in lang_map:
                result.append(lang_map[token])
            else:
                result.append(token)
        return ''.join(result)
    else:
        tokens = tokenize_phonetic(phonetic_text)
        syllables = parse_syllables_from_tokens(tokens)
        result = []
        for syl in syllables:
            if syl == ' ':
                result.append(' ')
            elif isinstance(syl, str):
                result.append(syl)
            else:
                converted = convert_syllable_script(syl, lang_data)
                result.append(converted)
        return ''.join(result)


def get_all_conversions(phonetic_text):
    conversions = {}
    for lang_key, lang_data in LANGUAGE_MAPS.items():
        conversions[lang_key] = {
            'name': lang_data['name'],
            'flag': lang_data['flag'],
            'phonetic': convert_to_language(phonetic_text, lang_key)
        }
    return conversions
